                <h2 class="fw-bold">THE SIX DOMAINS OF RESPONSIBLE COMPUTING</h2>
                <h3 class="fw-bold">RESPONSIBLE IMPACT</h3>

                <strong>Goals</strong>
                <ul>
                    <li>Offset the impact on the planet in the categories of ESG (Environmental, Social and Governmental)</li>
                    <li>Level the playing field through: Sustainability/Circularity, Diversity/Inclusion, Climate, Openness, Ethics</li>
                </ul>

                <strong>Approach</strong>
                <p>Six prime and measurable maturity characteristics represent the ability to achieve Responsible Impact:</p>
                <ul>
                    <li>Goal Setting</li>
                    <li>Scalability</li>
                    <li>Replicability</li>
                    <li>Socially-responsible business model/strategy</li>
                    <li>Measurable</li>
                    <li>Traceable to a United Nations Sustainability Development Goals (<a href="https://sdgs.un.org/goals" target="_blank">UN SDGs</a>)</li>
                </ul>

                <strong>Benefits</strong>
                <ul>
                    <li>Improved operational efficiencies across the board</li>
                    <li>Visibility as a global leader in responsible computing</li>
                    <li>Compliance with legislation, governance standards, and evolving cultural norms</li>
                </ul>

                <h3 class="fw-bold">RESPONSIBLE DATA CENTER</h3>
                <p>Data centers designed and operated with an emphasis on sustainability</p>

                <strong>Goals</strong>
                <ul>
                    <li>Reduce environmental impact with a more efficient strategy and design</li>
                    <li>Migrate to renewable energy sources</li>
                    <li>Monitor consumption and carbon footprint</li>
                    <li>Optimize re-use of waste from cooling and production</li>
                    <li>Strive to be net zero by 2030 in compliance with UN SDGs</li>
                </ul>

                <strong>Approach</strong>
                <ul>
                    <li>Reducing energy consumption with accurate measurements</li>
                    <li>Strategizing for technology refreshes</li>
                    <li>Implementing renewable energy sources</li>
                    <li>Identifying cooling considerations based on location</li>
                    <li>Focusing on cooling, power, distribution, UPS service, generators, cables, and racks with emphasis on use and
                        re-use of materials</li>
                </ul>

                <strong>Benefits</strong>
                <ul>
                    <li>Increased operational efficiency with environmental considerations</li>
                    <li>Reduced energy consumption</li>
                    <li>Decreased CO2 emissions</li>
                    <li>Less reliance on rare metals and toxic materials</li>
                </ul>

                <h3 class="fw-bold">RESPONSIBLE INFRASTRUCTURE</h3>
                <p>Efficient use of available and future technology</p>

                <strong>Goals</strong>
                <ul>
                    <li>Less reliance on rare metals and toxic materials</li>
                    <li>Realize greater efficiencies with infrastructure designed to deliver high-performing sustainable operations</li>
                    <li>Consolidate workloads that peak at different times to complement each other and increase efficient use of
                        resources</li>
                    <li>Obtain high levels of utilization (which can be improved by consolidation) to deliver more efficient use of
                        energy and resources</li>
                </ul>



                <strong>Approach</strong>
                <ul>
                    <li>Establish a baseline measurement on energy usage and material/asset lifecycle</li>
                    <li>Improve analysis and reporting based on experience and established baseline</li>
                    <li>Request each IT infrastructure vendor to provide information and reports on energy usage and product lifecycle</li>
                </ul>

                <strong>Benefits</strong>
                <ul>
                    <li>Savings in physical space</li>
                    <li>Reduced waste and consumption</li>
                    <li>Effective management of the ESG impact of hardware, software, and networks required for the development, testing, delivery, monitoring, control, and support of IT services</li>
                    <li>Consolidation and rationalization enabling individuals and organizations to have a positive impact on the world</li>
                </ul>

                <h3 class="fw-bold">RESPONSIBLE DATA USAGE</h3>
                <p>Data that is securely used in ways that drive transparency, fairness, and respect for the users</p>

                <strong>Goals</strong>
                <ul>
                    <li>Certify that the data used, processes, and people are trusted and high quality</li>
                    <li>Reduce errors and misinterpretation of data from manual handling by deploying intelligent workflows</li>
                    <li>Remain competitive and nimbly respond to quality data while embracing innovation from applying artificial
                        intelligence and machine learning</li>
                </ul>

                <strong>Approach</strong>
                <ul>
                    <li>More robust policies, guidelines, and practices for the governance of data (e.g., maintaining lineage and
                        explainability)</li>
                    <li>Ongoing data usage risk-assessment and risk mitigation with responsible computing in mind</li>
                    <li>Managing the lifecycle of data with accountable data-retention and destruction practices</li>
                    <li>Incident response and data breach remediation</li>
                </ul>

                <strong>Benefits</strong>
                <ul>
                    <li>Minimal errors in the data that could lead to financial losses, wrong executive decisions, or destroy the
                        integrity of individuals, enterprises, or even markets</li>
                    <li>Effective and efficient use of data</li>
                    <li>Heightened collaboration between the business and IT</li>
                </ul>

                <h3 class="fw-bold">RESPONSIBLE SYSTEMS</h3>
                <p>Inclusive systems that address bias and discrimination, driving equality for all</p>

                <strong>Goals</strong>
                <ul>
                    <li>Ensure the “systems” employ an integrated set of technologies to provide a service to human beings. It can be
                        composed of hardware, data, code, models, and services</li>
                    <li>Build systems that are ethical, privacy-preserving, secure, and resilient. Systems will be designed with the
                        environment, individuals, society, and the future taken into consideration in IT decisions</li>

                </ul>
                <strong>Approach</strong>

                Responsible Systems are designed with a 3 layered approach:
                <ul>
                    <li>A cultural ethos across the entire supporting organization</li>
                    <li>The use of forensic technology that can monitor, detect issues to enable trust</li>
                    <li>Governance requirements to which the entire organization adheres</li>
                </ul>

                <h2>Benefits</h2>
                <ul>
                    <li>Maintaining integrity of internal systems</li>
                    <li>Achieving compliance with internal and external standards</li>
                    <li>Ongoing monitoring to ensure companies develop and use responsible systems</li>
                    <li>Reinforced corporate social responsibility and closing the digital divide</li>
                </ul>

                <h3 class="fw-bold">RESPONSIBLE CODE</h3>
                <p>Conscious code choices that optimize environmental, social, and economic impact over time</p>

                <strong>Goals</strong>
                <ul>
                    <li>Align teams on software architecture, technology, programming language, and platform</li>
                    <li>Anticipate and monitor the total costs of running, supporting and maintaining applications</li>
                    <li>Balance the trade-off between accuracy, speed, and cost, including energy consumption of systems</li>
                    <li>Address the hidden energy impact of code</li>
                    <li>Reduce data duplication and improve cybersecurity</li>
                </ul>

                <strong>Approach</strong>
                <ul>
                    <li>Implement sustainability maturity assessment tools and KPIs to accelerate decision-making and pinpoint areas
                        requiring more scrutiny during software development</li>
                    <li>Utilize tools and methods to apply to develop applications more responsibly</li>
                    <li>Heighten team awareness by setting up ongoing training and workshops to reinforce shared sustainability
                        goals</li>
                </ul>

                <strong>Benefits</strong>
                <ul>
                    <li>Conscious design choices reflecting accessibility, energy consumption, and economic sustainability</li>
                    <li>Improved cybersecurity</li>
                    <li>Increased awareness of the responsibility that comes with writing code and the impacts on users’ lives and the
                        planet</li>
                    <li>Expose the hidden energy impact of code and decreasing the digital divide</li>
                </ul>
                </p>



