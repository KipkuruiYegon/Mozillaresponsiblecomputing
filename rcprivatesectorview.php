                <h2 class="mt-1 fw-bold" style="">RESPONSIBLE COMPUTING: FROM A PRIVATE SECTOR STAKEHOLDER LENS</h2>

                <p class="">Responsible Computing? As we mingled and chimed glasses, during the recent stakeholder
                    workshop on Mozilla Responsible Computing Challenge – Dedan Kimathi University of
                    Technology (DeKUT) Chapter, that question rung through the air. Amidst dissimilar postulations,
                    at least, there was a unanimous supposition that being responsible is a virtue of life that an
                    individual ought to practice right from the early ages of cognitive development. In fact,
                    Erickson’s early theory of human growth and development posit that by the age of 3 years,
                    children start developing a sense of purpose, initiative, guilty and egocentricism that inform their
                    perception of the world. From the foregoing it would be imagined, then, that people would
                    naturally exercise acts that advance ethical and responsible behaviour, but that has not been the
                    case. Our modern-day society is riddle with irresponsible actions, including irresponsible
                    computing practices.
                <p>
                    Even as I was writing this article, my ears covered with air pods listening to music, severally I
                    was being rudely interrupted by a text alert from a phishing app requesting if I have any financial
                    pressure and needed some easy money. Yes, I have non-shareable financial pressure, just like
                    many of you, but many questions linger on how irresponsible the developers and custodians of
                    such apps have become? Such similar unsolicited prompts fill our phones and computers,
                    nowadays, disregarding simple computing principles of privacy and responsible computing.
                </p>
                <p>
                    So, what is responsible computing and why should we care? From an academic perspective, Dr.
                    Gikunda and Dr. Kituku both lecturers at DeKUT School of Computer Science &amp; Information
                    Technology agree that the definition of RC should be broad and multidimensional. In fact, they
                    posit that it is impossible to pin point ethics as the only and ultimate mark up for RC. As the
                    intellectual debate ensued, Mr. Naivasha, a lecturer in the same school, questioned the
                    synonymity between ethics and RC? From the resultant debate, between the faculty members
                    there was a consensus that ethics plays a critical role in an individual’s RC behaviour. In fact, Dr.
                    Gikunda stressed that ethics is a subset of RC and we ought to teach and come up with a strong
                    code of ethics to govern responsible computing practices in our society. Obviously, it is easy to
                    conclude that faculty members believe that RC is an inward facing subject matter.
                </p>

                <p>However, while that inward facing approach may sound true and complete, private sector players
                    provide a different outward facing perspective. Jude Juma a software engineer at Safaricom
                    PLC, reckons that RC is exercising computer best practices, with considerations of sustainability,
                    efficiency and considerate power consumptions. While Steve Mambo, the CEO at YelBridges, a
                    company that deals with information security, asserts that although RC starts with computer
                    science and information technology developers, it goes beyond developers to the end users. He
                    poses that most apps, are designed with good intentions, but the users of these apps use them for
                    hedonistic gains and self-gratifying business models. He strongly recommends that a good
                    comprehension of RC should include responsible consumption. In support of this outward facing
                    approach of defining RC, Simeon Rutto, Head of Forensic Science at Britam group and Co-
                    Founder Data Forensics and Training Consulting firm consulting firm affirms that unethical
                    behaviour continues to pervade our corporate spaces and is mainly facilitated by the end users oftechnology.
                    From this perspective the user of technology bear burden of ensuring responsibility
                    in technology use.
                </p>

                <p>
                    In a more progressive thinking, Dr. George Musumba, the principal investigator in the DeKUT
                    Mozilla project and Dean School of Computer Science and IT, asserts that whether inward facing
                    or outward facing, RC is a concern in the society and immediate action should be undertaken.
                    According to Mozilla foundation, many technologies have facilitated new forms of connection
                    and creativity, the benefits and harms of these technologies have not been distributed equally.
                    And with this increased digital surveillance, automated discrimination, and amplified
                    misinformation computing technologies could harm the most vulnerable populations in our
                    society. Jobin Kiriswa a project manager at MOVE inc. a digital cab and logistic hailing app,
                    reckons that there is an opportunity to make it right. He poses that, different stakeholders have a
                    role to advance RC from a developer and/or consumer end.
                </p>
                <p>
                    But what can the academia do? Dr. Musumba, believes that ingenious curricular edits can
                    support the RC movement while Kagiri a software engineering lecturer and lead systems
                    developer believes that training early developers on RC can shift their perception. Similarly, Mr.
                    Muchina, Mr. Muchiri and Dr. Kituku (both lecturers) argue that unique approaches can be
                    adopted in teaching RC in the computing program. In a parallel rejoinder, Dr. Omari, Chair of
                    Department Actuarial Science at DeKUT, believes there is an opportunity to teach integrity,
                    ethical and RC computing concepts in our programs across all the units.
                </p>

